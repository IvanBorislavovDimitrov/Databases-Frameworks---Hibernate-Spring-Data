@XmlAccessorType(XmlAccessType.FIELD)
public class CategoryDto {

    @XmlElement(name = "name")
    private String name;

    public CategoryDto() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}